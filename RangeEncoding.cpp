class RangeEncoding {
public:
	int minRanges(vector <int>);
};

int RangeEncoding::minRanges(vector <int> arr) {
	int max = 1;
	for(int i=1;i<arr.size();i++){
		if(arr[i]!=arr[i-1]+1) max+=1;
	}
	return max;
}

