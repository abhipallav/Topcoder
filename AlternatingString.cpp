class AlternatingString {
public:
	int maxLength(string);
};

int AlternatingString::maxLength(string s) {
	int maxLen = 1;
	int currLen = 1;
	
	for(int i=1;i<s.size();i++){
		if(s[i]!=s[i-1]){
			currLen += 1;
		} else {
			currLen = 1;
		}
		
		if(currLen > maxLen){
			maxLen = currLen;
		}
	}
	
	return maxLen;
}

