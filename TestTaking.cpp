class TestTaking {
public:
	int findMax(int, int, int);
};

int TestTaking::findMax(int questions, int guessed, int actual) {
	int falseAns = questions-actual;
	int result = min(guessed, actual);
	int wrongAns = max(guessed-actual, 0);
	
	return (result + falseAns - wrongAns);
}

